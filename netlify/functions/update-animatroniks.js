let prisma;

// 1. connect to prisma, only once
if (!prisma) {
  prisma = new PrismaClient();
  prisma.$connect();
}

const handler = async function () {
  async function getAnimatroniks() {
    return prisma.animatronik.findMany();
  }

  async function createAnimatronik(animatronik) {
    return prisma.animatronik.create({ data: animatronik });
  }

  // 2. instance animatronik contract
  const ANIMATRONIK_OPTIMISM_GOERLI_ADDRESS =
    "0xfa69459bFBff37E818498af01Ff0ADe85A8083c6";
  const provider = new JsonRpcProvider(
    "https://opt-goerli.g.alchemy.com/v2/BBYbDq6HCJ5YNwERxUQqtsO9Uy6_c-UU",
    "any"
  );
  const animatronikContract = new Contract(
    ANIMATRONIK_OPTIMISM_GOERLI_ADDRESS,
    _abi,
    provider
  );

  // 3. generate next token id
  const animatronikCount = (
    await animatronikContract.getAnimatronikCount()
  ).toNumber();
  const currentAnimatronikCount = (await getAnimatroniks()).length;
  const nextTokenId =
    currentAnimatronikCount === animatronikCount
      ? null
      : currentAnimatronikCount;

  // if there is no next token id, just return
  console.log(nextTokenId ? `Trying token id => ${nextTokenId}` : "Skipping");
  if (typeof nextTokenId !== "number") {
    return {
      statusCode: 200,
    };
  }

  // 4. if there is one, save it
  const [css, svg] = await animatronikContract.animatroniks(nextTokenId);
  const animatronik = await createAnimatronik({
    id: nextTokenId,
    css,
    svg,
  });

  console.log("Created animatronik =>", animatronik);

  return {
    statusCode: 201,
  };
};

// every minute
module.exports.handler = schedule("* * * * *", handler);
